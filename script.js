function createGameItem(game) {
  const li = document.createElement("li");
  li.className = "game-item";
  li.innerHTML = `
    <button class="favorite-btn" onclick="toggleFavorite('Game 50')">
      <span id="fav-Game 50">♡</span>
    </button>
    <img src="${game.thumbnail}" alt="${game.name}">
    <h2>${game.name}</h2>
    <p>${game.description}</p>
    <a href="${game.link}" target="_blank" class="play-button">Play Now</a>
  `;
  return li;
}

function renderGames(filter = "") {
  const gameList = document.getElementById("gameList");
  gameList.innerHTML = "";
  const category = document.getElementById("categoryFilter").value;
  games
    .filter(game =>
      game.name.toLowerCase().includes(filter.toLowerCase()) &&
      (category === "All" || game.category === category)
    )
    .forEach(game => {
      gameList.appendChild(createGameItem(game));
      updateFavoriteIcon(game.name);
    });
}

function toggleFavorite(name) {
  const favorites = JSON.parse(localStorage.getItem("favorites") || "[]");
  const index = favorites.indexOf(name);
  if (index >= 0) {
    favorites.splice(index, 1);
  } else {
    favorites.push(name);
  }
  localStorage.setItem("favorites", JSON.stringify(favorites));
  updateFavoriteIcon(name);
}

function updateFavoriteIcon(name) {
  const favorites = JSON.parse(localStorage.getItem("favorites") || "[]");
  const span = document.getElementById(`fav-${name}`);
  if (span) {
    span.textContent = favorites.includes(name) ? "❤️" : "♡";
    span.className = favorites.includes(name) ? "favorite-active" : "";
  }
}

document.getElementById("searchBox").addEventListener("input", e => {
  renderGames(e.target.value);
});

document.getElementById("categoryFilter").addEventListener("change", () => {
  renderGames(document.getElementById("searchBox").value);
});

document.getElementById("darkModeToggle").addEventListener("click", () => {
  document.body.classList.toggle("dark-mode");
});

renderGames();
